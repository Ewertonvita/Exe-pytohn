from math import sqrt,trunc# import a função raiz quadrada sqrt com formatação floor
n = int (input(" digite um número = ")) # variavel n inteiro entra um numero.
raiz = sqrt(n)# outra variavel receba a --raiz quadrada--  de n
print (trunc(raiz)) # na tela a variavel raiz recebe o raiz quadra da de "n" com formatação floor arredonda para baixo
b =  int(input("digite  outro numero = "))
raiz1 = sqrt (b) # raiz quadrada normal 
print (raiz1) # aqui aparece a raiz normal, coloque 2 números iguais para ver!
